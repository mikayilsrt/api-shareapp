    public function countByFavoriteAndUser($photo, $user)
    {
        $qb = $this->createQueryBuilder('f')
            ->select('COUNT(f)')
            ->where('f.photo = :photo')
            ->andWhere('f.user = :user')
            ->setParameter('photo', $photo)
            ->setParameter('user', $user)
        ;

        return $qb->getQuery()->getSingleScalarResult();
    }

